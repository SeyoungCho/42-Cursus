#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;
	int		i;
	int		len;

	i = 0;
	len = ft_strlen(s1);
	ret = (char *)malloc(sizeof(char) * (len + 1));
	if (!ret)
		return (0);
	while (i < len)
	{
		ret[i] = s1[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
